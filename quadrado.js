// Dado o tamanho do lado de um quadrado, calcular a área e o perímetro do mesmo

let ladoQuadrado = 8;

const perimetroQuadrado = ladoQuadrado * 4;

const areaQuadrado = ladoQuadrado * ladoQuadrado;

console.log(`Perímetro do quadrado: ${perimetroQuadrado}`);

console.log(`Área do quadrado: ${areaQuadrado}`);

